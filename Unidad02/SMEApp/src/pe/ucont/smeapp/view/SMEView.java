	private void proceso() {
		String dato;
		switch (op) {
			case 1:
				String menu = "";
				menu += "MENU\n";
				menu += "=======================\n";
				menu += "1.- Soles a dolares\n";
				menu += "2.- Dolares a soles\n";
				menu += "3.- Soles a euros\n";
				menu += "4.- Euros a soles\n";
				menu += "=======================\n";
				menu += "Opción? ";
				txtConsola.setText(menu);
				op = 2;
				break;
			case 2:
				opMenu = Integer.parseInt(entrada);
				dato = txtConsola.getText();
				if (opMenu < 1 || opMenu > 4) {
					dato += "\nOpción incorrecta.\n";
					dato += "Opción? ";
				} else {
					dato += "\nImporte? ";
					op = 3;
				}
				txtConsola.setText(dato);
				entrada = "";
				break;
			case 3:
				importeOrigen = Double.parseDouble(entrada);
				importeDestino = obtenerCambio(opMenu, importeOrigen);
				dato = txtConsola.getText();
				dato += "\nCambio: " + importeDestino + "\n";
				txtConsola.setText(dato);
				entrada = "";
				op = 4;
		}
	}

	private double obtenerCambio(int opMenu, double valor) {
		SMEService service = new SMEService();
		double cambio = 0.0;
		switch (opMenu) {
			case 1:
				cambio = service.solesToDolares(valor);
				break;
			case 2:
				cambio = service.dolaresToSoles(valor);
				break;
			case 3:
				cambio = service.solesToEuros(valor);
				break;
			case 4:
				cambio = service.eurosToSoles(valor);
				break;
		}
		return cambio;
	}

